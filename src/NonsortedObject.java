
public class NonsortedObject {
	public char firstLetter;
	public int firstFreq;
	public char secondLetter;
	public int secondFreq;
	
	public NonsortedObject(char firstLetter, int firstFreq, char secondLetter, int secondFreq) {
		this.firstFreq = firstFreq;
		this.firstLetter = firstLetter;
		this.secondFreq = secondFreq;
		this.secondLetter = secondLetter;
	}

}
